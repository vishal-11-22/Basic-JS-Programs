let n1=45,n2=56;
let op='+'
switch(op)
{
    case '+': console.log(n1+n2);
                break;
    case '-': console.log(n1-n2);
                break;
    case '*': console.log(n1*n2);
                break;
    case '/': console.log(n1/n2);
                break;
    case '%': console.log(n1%n2);
                break;
}